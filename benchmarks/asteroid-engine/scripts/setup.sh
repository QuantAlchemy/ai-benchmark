#!/usr/bin/env bash
# Fetch the original engine + demo source, pinned to exact commits, into $BENCH_SOURCE.
# Re-runnable: pass --force to refetch. Strips VCS history and stale build artifacts
# but keeps game assets (Resources/) so the demo can actually run.
set -euo pipefail

SRC="${BENCH_SOURCE:?BENCH_SOURCE not set (run via: pnpm bench setup asteroid-engine)}"
FORCE="${1:-}"

# name|url|commit
REPOS=(
  "Game_Engine|https://github.com/ralphsmith80/Game_Engine|6ad9544638c247de0ead285a5e36b244949cf036"
  "Asteroid_Demo|https://github.com/ralphsmith80/Asteroid_Demo|94567640000cf0ad7fdfef95b0ea5cd972c1d281"
)

fetch_pinned() {
  local name="$1" url="$2" sha="$3" dest="$SRC/$1"
  if [[ -d "$dest" && "$FORCE" != "--force" ]]; then
    echo "  • $name already present (use --force to refetch) — skipping"
    return 0
  fi
  rm -rf "$dest"
  mkdir -p "$dest"
  echo "  • fetching $name @ ${sha:0:10} …"
  (
    cd "$dest"
    git init -q
    git remote add origin "$url"
    # GitHub allows fetching arbitrary SHAs; fall back to a full fetch if not.
    if ! git fetch -q --depth 1 origin "$sha" 2>/dev/null; then
      echo "    (shallow SHA fetch unsupported; doing a full fetch)"
      git fetch -q origin
    fi
    git checkout -q "$sha"
    rm -rf .git
  )
}

strip_artifacts() {
  local dir="$1"
  # Stale compiler/IDE output — regenerated by a build, never part of "source".
  find "$dir" -depth \( \
        -iname 'Debug' -o -iname 'Release' -o -iname '.vs' -o -iname 'build' \
     \) -type d -exec rm -rf {} + 2>/dev/null || true
  find "$dir" -type f \( \
        -iname '*.ncb' -o -iname '*.suo' -o -iname '*.pdb' -o -iname '*.user' \
     -o -iname '*.o'   -o -iname '*.obj' -o -iname '*.ilk' -o -iname '*.idb' \
     -o -name 'asteroids_mac' -o -name 'asteroids_linux' -o -name 'asteroids' \
     \) -delete 2>/dev/null || true
}

echo "Fetching pinned source into: $SRC"
mkdir -p "$SRC"
for entry in "${REPOS[@]}"; do
  IFS='|' read -r name url sha <<< "$entry"
  fetch_pinned "$name" "$url" "$sha"
  strip_artifacts "$SRC/$name"
done

cat > "$SRC/README.FETCHED.md" <<'EOF'
# Original source (fetched, do not edit)

This directory is materialized by `bench setup asteroid-engine` from pinned commits
and is read-only reference input for the benchmark.

- `Game_Engine/`  — the standalone 2D engine + a reference desktop demo.
- `Asteroid_Demo/` — the packaged demo (desktop `Asteroid_Demo/` + `Asteroid_Demo_iPhone/`).

Build artifacts (Debug/, *.ncb, *.suo, prebuilt binaries) were stripped; game assets
under `Resources/` are kept because the demo needs them at runtime.

The model's modernized port goes in `../../../solutions/asteroid-engine/`, not here.
EOF

echo "Done."
