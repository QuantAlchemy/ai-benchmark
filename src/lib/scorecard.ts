import { formatMetricBytes, formatMetricDuration, type RunMetrics } from "./metrics";

export type ScorecardCriterion = {
  id: string;
  index: number;
  name: string;
  description: string;
  weight: number;
  score: number | null;
  notes: string;
};

export type ScorecardCheck = {
  id: string;
  label: string;
  checked: boolean;
};

export type ScorecardData = {
  version: 1;
  title: string;
  scale: string;
  criteria: ScorecardCriterion[];
  checks: ScorecardCheck[];
};

export function createScorecardData(rubric: string): ScorecardData {
  const title = rubric.match(/^#\s+(.+)$/m)?.[1]?.trim() ?? "Scorecard";
  const scale = rubric.match(/\*\*Score scale:\*\*\s*([\s\S]*?)(?:\n\n|\n\|)/)?.[1]?.replace(/\s+/g, " ").trim() ?? "";
  const criteria = parseCriteria(rubric);
  const checks = parseChecks(rubric);

  return {
    version: 1,
    title,
    scale,
    criteria,
    checks,
  };
}

export function normalizeScorecardData(value: unknown, fallbackRubric = ""): ScorecardData {
  if (value && typeof value === "object") {
    const candidate = value as Partial<ScorecardData>;
    if (Array.isArray(candidate.criteria)) {
      return {
        version: 1,
        title: String(candidate.title || "Scorecard"),
        scale: String(candidate.scale || ""),
        criteria: candidate.criteria.map((criterion, index) => ({
          id: String(criterion.id || `criterion-${index + 1}`),
          index: Number(criterion.index || index + 1),
          name: String(criterion.name || `Criterion ${index + 1}`),
          description: String(criterion.description || ""),
          weight: Number(criterion.weight || 1),
          score: typeof criterion.score === "number" ? clampScore(criterion.score) : null,
          notes: String(criterion.notes || ""),
        })),
        checks: Array.isArray(candidate.checks)
          ? candidate.checks.map((check, index) => ({
              id: String(check.id || `check-${index + 1}`),
              label: String(check.label || `Check ${index + 1}`),
              checked: Boolean(check.checked),
            }))
          : [],
      };
    }
  }

  return createScorecardData(fallbackRubric);
}

export function scorecardTotal(data: ScorecardData) {
  return data.criteria.reduce((total, criterion) => total + (criterion.score ?? 0) * criterion.weight, 0);
}

export function scorecardMax(data: ScorecardData) {
  return data.criteria.reduce((total, criterion) => total + 5 * criterion.weight, 0);
}

export function scorecardScored(data: ScorecardData) {
  return data.criteria.some((criterion) => typeof criterion.score === "number");
}

export function scorecardPercent(data: ScorecardData) {
  const max = scorecardMax(data);
  if (!max) return null;
  return (scorecardTotal(data) / max) * 100;
}

export function renderScorecardMarkdown(input: {
  benchmarkName: string;
  benchmarkId: string;
  scoreModel: string;
  createdAt: string;
  data: ScorecardData;
  metrics?: RunMetrics | null;
  notes: string;
}) {
  const total = scorecardTotal(input.data);
  const max = scorecardMax(input.data);
  const lines = [
    "<!-- Scorecard generated by ai-benchmark -->",
    `# Scorecard - ${input.benchmarkName}`,
    "",
    `- **Model:** ${input.scoreModel}`,
    `- **Date:** ${input.createdAt.slice(0, 10)}`,
    `- **Benchmark:** ${input.benchmarkId}`,
    `- **Total:** ${total}/${max}`,
    "",
  ];

  if (input.data.scale) {
    lines.push(`**Score scale:** ${input.data.scale}`, "");
  }

  lines.push("| # | Criterion | Weight | Score | Weighted | Notes |");
  lines.push("|---|-----------|:------:|:-----:|:--------:|-------|");
  for (const criterion of input.data.criteria) {
    const score = criterion.score ?? "";
    const weighted = typeof criterion.score === "number" ? criterion.score * criterion.weight : "";
    lines.push(
      `| ${criterion.index} | **${escapeCell(criterion.name)}**<br>${escapeCell(criterion.description)} | x${criterion.weight} | ${score} | ${weighted} | ${escapeCell(criterion.notes)} |`,
    );
  }
  lines.push(`| | **Total** | | | **${total}/${max}** | |`, "");

  if (input.data.checks.length) {
    lines.push("## Checks", "");
    for (const check of input.data.checks) {
      lines.push(`- [${check.checked ? "x" : " "}] ${check.label}`);
    }
    lines.push("");
  }

  if (input.metrics) {
    const metrics = input.metrics;
    lines.push("## Quantitative Metrics (auto-measured)", "");
    lines.push(`- **Agent run time:** ${formatMetricDuration(metrics.agentDurationMs)}`);
    if (metrics.solutionSize) {
      lines.push(
        `- **Solution size:** ${metrics.solutionSize.files} files, ${metrics.solutionSize.lines} lines, ${formatMetricBytes(metrics.solutionSize.bytes)}`,
      );
    }
    if (metrics.verify) {
      lines.push(
        `- **Verify:** ${metrics.verify.ok ? "pass" : "fail"} (exit ${metrics.verify.exitCode}, ${formatMetricDuration(metrics.verify.durationMs)}, ${metrics.verify.measuredAt.slice(0, 19)})`,
      );
    }
    if (metrics.launch) {
      lines.push(
        `- **Launch:** ${metrics.launch.ok ? "responded" : "no HTTP response"}${metrics.launch.url ? ` at ${metrics.launch.url}` : ""}${metrics.launch.timeToReadyMs !== null ? ` in ${formatMetricDuration(metrics.launch.timeToReadyMs)}` : ""}`,
      );
    }
    lines.push("");
  }

  lines.push("## Notes", "", input.notes || "_No notes recorded._", "");
  return lines.join("\n");
}

function parseCriteria(rubric: string): ScorecardCriterion[] {
  return rubric
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => /^\|\s*\d+\s*\|/.test(line))
    .map((line, index) => {
      const cells = line
        .slice(1, -1)
        .split("|")
        .map((cell) => cleanMarkdown(cell.trim()));
      return {
        id: slugify(cells[1] || `criterion-${index + 1}`),
        index: Number(cells[0]) || index + 1,
        name: cells[1] || `Criterion ${index + 1}`,
        description: cells[2] || "",
        weight: Number(cells[3]?.replace(/[^0-9.]/g, "")) || 1,
        score: null,
        notes: "",
      };
    });
}

function parseChecks(rubric: string): ScorecardCheck[] {
  return rubric
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => /^-\s+\[[ xX]\]\s+/.test(line))
    .map((line, index) => {
      const checked = /^-\s+\[[xX]\]/.test(line);
      const label = cleanMarkdown(line.replace(/^-\s+\[[ xX]\]\s+/, ""));
      return {
        id: slugify(label || `check-${index + 1}`),
        label,
        checked,
      };
    });
}

function cleanMarkdown(value: string) {
  return value
    .replace(/<br\s*\/?>/gi, " ")
    .replace(/[`*_]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function slugify(value: string) {
  return cleanMarkdown(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "")
    .slice(0, 80);
}

function clampScore(score: number) {
  return Math.max(0, Math.min(5, score));
}

function escapeCell(value: string) {
  return value.replace(/\|/g, "\\|").replace(/\n/g, "<br>");
}
